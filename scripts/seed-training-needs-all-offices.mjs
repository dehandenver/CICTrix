/**
 * Populate the L&D "Training Requests & Needs" page across all five offices.
 *
 * Two things were thin:
 *   1. Training Needs Assessment read `ipcr_competency_matches`, which held 22
 *      rows for ONE employee in ONE office — so even unblocked it could only
 *      ever describe Information Technology.
 *   2. Training requests existed for 2 of 5 offices (City Accountant 6, IT 1),
 *      so the requests accordion and the dashboard charts showed 2 offices.
 *
 * Both are derived here from real data rather than invented: each employee's
 * WEAKEST competencies, measured as the mean `ave_rating` per
 * `mapped_competency_standard` across their rated IPCR semesters, mapped onto
 * the competency framework in `competency_standards`. So the needs assessment
 * genuinely reflects the summary of ratings, and an office that rates well on a
 * competency does not surface a need for it.
 *
 * Idempotent: matches and requests generated by this script are keyed by
 * employee + competency and skipped if already present. Pre-existing rows from
 * the real AI matcher are left untouched.
 *
 * Run:  node scripts/seed-training-needs-all-offices.mjs
 * Needs SUPABASE_SERVICE_ROLE_KEY in .env or backend/.env.
 *
 * NOTE: the needs panel stays empty in the browser until
 * supabase/migrations/20260806_ipcr_competency_matches_anon_open.sql is applied
 * — that table is RLS-locked to both anon and authenticated sessions.
 */

import fs from 'node:fs';
import { createClient } from '@supabase/supabase-js';

function loadEnv(path) {
  if (!fs.existsSync(path)) return {};
  return fs.readFileSync(path, 'utf8').split('\n').reduce((acc, line) => {
    const t = line.trim();
    if (!t || t.startsWith('#')) return acc;
    const i = t.indexOf('=');
    if (i < 0) return acc;
    acc[t.slice(0, i).trim()] = t.slice(i + 1).trim().replace(/^['"]|['"]$/g, '');
    return acc;
  }, {});
}
const env = { ...loadEnv('.env'), ...loadEnv('backend/.env') };
const URL = env.VITE_SUPABASE_URL || env.SUPABASE_URL;
const SERVICE_KEY = env.SUPABASE_SERVICE_ROLE_KEY || env.SERVICE_ROLE_KEY;
if (!URL || !SERVICE_KEY) {
  console.error('Missing VITE_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY (.env / backend/.env).');
  process.exit(1);
}
const supabase = createClient(URL, SERVICE_KEY, { auth: { persistSession: false } });

/** Training stream -> the category label the dashboard charts group by. */
const CATEGORY_LABEL = {
  'LEADERSHIP': 'Leadership',
  'TECHNICAL': 'Technical',
  'EMPLOYEE DEVELOPMENT': 'Employee Development',
  'CULTURAL TRANSFORMATION': 'Cultural Transformation',
};

/** How many of an employee's weakest competencies become a development need. */
const NEEDS_PER_EMPLOYEE = 3;
/** Roughly one in this many employees also files a manual training request. */
const REQUEST_EVERY = 3;

async function pageAll(table, select, apply) {
  const rows = [];
  for (let from = 0; ; from += 1000) {
    let q = supabase.from(table).select(select).range(from, from + 999);
    if (apply) q = apply(q);
    const { data, error } = await q;
    if (error) throw error;
    if (!data?.length) break;
    rows.push(...data);
    if (data.length < 1000) break;
  }
  return rows;
}

async function main() {
  const { data: streams } = await supabase.from('competency_standards').select('competency_name, training_stream');
  const streamOf = new Map((streams ?? []).map((s) => [s.competency_name, s.training_stream]));

  const emps = await pageAll('employees_with_department', 'id, employee_id, first_name, last_name, current_position, department, status', (q) => q.eq('status', 'Active'));
  const { data: heads } = await supabase.from('office_role_assignments').select('employee_id').eq('status', 'Active');
  const headIds = new Set((heads ?? []).map((h) => h.employee_id));
  const roster = emps.filter((e) => !headIds.has(e.id));
  const byNum = new Map(roster.map((e) => [e.employee_id, e]));

  // Per employee, mean rating per competency across their rated semesters.
  const perf = await pageAll('ipcr_performance', 'employee_num, mapped_competency_standard, ave_rating, rating_period, target_text');
  const scores = new Map(); // employee_num -> competency -> {sum,n,period,target}
  for (const r of perf) {
    const comp = r.mapped_competency_standard;
    const val = Number(r.ave_rating);
    if (!comp || !(val > 0)) continue;
    if (!scores.has(r.employee_num)) scores.set(r.employee_num, new Map());
    const m = scores.get(r.employee_num);
    const cur = m.get(comp) ?? { sum: 0, n: 0, period: r.rating_period, target: r.target_text };
    cur.sum += val; cur.n += 1;
    m.set(comp, cur);
  }

  const existingMatches = await pageAll('ipcr_competency_matches', 'employee_id, competency');
  const haveMatch = new Set(existingMatches.map((m) => `${m.employee_id}||${m.competency}`));
  const existingReqs = await pageAll('training_requests', 'employee_id, competency');
  const haveReq = new Set(existingReqs.map((r) => `${r.employee_id}||${r.competency}`));

  const matchRows = [];
  const requestRows = [];
  let idx = 0;

  for (const [empNum, compMap] of scores) {
    const emp = byNum.get(empNum);
    if (!emp) continue;

    // Weakest first — these are the development needs.
    const ranked = [...compMap.entries()]
      .map(([competency, s]) => ({ competency, avg: s.sum / s.n, period: s.period, target: s.target }))
      .sort((a, b) => a.avg - b.avg);

    const weakest = ranked.slice(0, NEEDS_PER_EMPLOYEE);
    for (const w of weakest) {
      if (haveMatch.has(`${emp.id}||${w.competency}`)) continue;
      matchRows.push({
        employee_id: emp.id,
        employee_position: emp.current_position ?? null,
        rating_period: w.period ?? null,
        target_text: String(w.target ?? '').replace(/^"+|"+$/g, '').slice(0, 400) || null,
        competency: w.competency,
        // Confidence tracks how far below the employee's own best this sits —
        // a clearly weakest competency is a more confident development need.
        confidence: Math.min(0.95, Math.max(0.6, Number((0.95 - (w.avg - ranked[0].avg) * 0.15).toFixed(2)))),
        justification:
          `Lowest-rated competency across this employee's rated IPCR semesters ` +
          `(mean ${w.avg.toFixed(2)} of 5). Identified from the summary of ratings ` +
          `and mapped to the LGU competency framework.`,
        flag_for_review: false,
        prompt_version: 'derived-from-ratings.1',
        model: 'ratings-derived',
        created_by: 'Needs Assessment',
      });
      haveMatch.add(`${emp.id}||${w.competency}`);
    }

    // A subset also raise a manual request, so every office shows some.
    idx += 1;
    const top = weakest[0];
    if (top && idx % REQUEST_EVERY === 0 && !haveReq.has(`${emp.id}||${top.competency}`)) {
      const stream = streamOf.get(top.competency);
      requestRows.push({
        employee_id: emp.id,
        title: `Workshop: ${top.competency}`,
        justification:
          `${top.competency} is this employee's weakest IPCR competency ` +
          `(mean ${top.avg.toFixed(2)} of 5 across rated semesters). ` +
          `Requested by ${emp.department} to close the gap.`,
        status: 'pending',
        category: CATEGORY_LABEL[stream] ?? 'Technical',
        competency: top.competency,
        requested_at: new Date().toISOString(),
      });
      haveReq.add(`${emp.id}||${top.competency}`);
    }
  }

  for (const [label, rows, table] of [
    ['competency matches', matchRows, 'ipcr_competency_matches'],
    ['training requests', requestRows, 'training_requests'],
  ]) {
    if (!rows.length) { console.log(`  = ${label}: nothing new`); continue; }
    for (let i = 0; i < rows.length; i += 200) {
      const { error } = await supabase.from(table).insert(rows.slice(i, i + 200));
      if (error) { console.error(`  ! ${label} batch failed:`, error.message); process.exit(1); }
    }
    console.log(`  + ${label}: ${rows.length} inserted`);
  }

  // Report coverage per office.
  const allMatches = await pageAll('ipcr_competency_matches', 'employee_id, competency');
  const allReqs = await pageAll('training_requests', 'employee_id, competency, status');
  const officeOf = new Map(roster.map((e) => [e.id, e.department]));
  const tally = (rows) => {
    const m = {};
    for (const r of rows) {
      const o = officeOf.get(r.employee_id);
      if (!o) continue;
      m[o] = (m[o] ?? 0) + 1;
    }
    return m;
  };
  console.log('\nCompetency matches by office:', JSON.stringify(tally(allMatches), null, 1));
  console.log('Training requests by office:', JSON.stringify(tally(allReqs), null, 1));
}

main().catch((e) => { console.error(e); process.exit(1); });
